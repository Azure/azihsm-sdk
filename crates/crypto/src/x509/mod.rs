//! X.509 certificate encoding and decoding utilities.
//!
//! This module provides functions for converting between DER and PEM formats
//! for X.509 certificates using the `pem-rfc7468` crate.

use crate::CryptoError;

/// Convert DER-encoded certificate bytes to PEM format.
///
/// # Arguments
///
/// * `der` - The DER-encoded certificate bytes
///
/// # Returns
///
/// Returns the PEM-encoded certificate as a String with LF line endings.
///
/// # Errors
///
/// Returns `CryptoError::DerAsn1EncodeError` if encoding fails.
pub fn der_to_pem(der: &[u8]) -> Result<String, CryptoError> {
    pem_rfc7468::encode_string("CERTIFICATE", pem_rfc7468::LineEnding::LF, der)
        .map_err(|_| CryptoError::DerAsn1EncodeError)
}

/// Convert PEM-encoded certificate to DER format.
///
/// # Arguments
///
/// * `pem` - The PEM-encoded certificate as bytes
///
/// # Returns
///
/// Returns the DER-encoded certificate as a Vec<u8>.
///
/// # Errors
///
/// Returns `CryptoError::DerAsn1DecodeError` if:
/// - The PEM format is invalid
/// - The PEM type label is not "CERTIFICATE"
/// - UTF-8 decoding fails
pub fn pem_to_der(pem: &[u8]) -> Result<Vec<u8>, CryptoError> {
    let pem_str = std::str::from_utf8(pem).map_err(|_| CryptoError::DerAsn1DecodeError)?;

    let (label, der) =
        pem_rfc7468::decode_vec(pem_str.as_bytes()).map_err(|_| CryptoError::DerAsn1DecodeError)?;

    // Verify this is a certificate PEM
    if label != "CERTIFICATE" {
        return Err(CryptoError::DerAsn1DecodeError);
    }

    Ok(der)
}

