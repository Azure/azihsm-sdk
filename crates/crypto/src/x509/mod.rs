    #[test]
    fn test_der_to_pem() {
        let pem = der_to_pem(VALID_DER).expect("Failed to convert DER to PEM");

        // Verify the PEM starts and ends with the correct markers
        assert!(pem.starts_with("-----BEGIN CERTIFICATE-----\n"));
        assert!(pem.ends_with("-----END CERTIFICATE-----\n"));
    }

    #[test]
    fn test_pem_to_der() {
        let der = pem_to_der(VALID_PEM.as_bytes()).expect("Failed to convert PEM to DER");

        // Verify we got some DER data
        assert!(!der.is_empty());
        // DER certificates start with SEQUENCE tag (0x30)
        assert_eq!(der[0], 0x30);
    }

    #[test]
    fn test_roundtrip_der_pem_der() {
        // DER -> PEM -> DER
        let pem = der_to_pem(VALID_DER).expect("Failed to convert DER to PEM");
        let der = pem_to_der(pem.as_bytes()).expect("Failed to convert PEM back to DER");

        assert_eq!(der, VALID_DER);
    }

    #[test]
    fn test_roundtrip_pem_der_pem() {
        // PEM -> DER -> PEM
        let der = pem_to_der(VALID_PEM.as_bytes()).expect("Failed to convert PEM to DER");
        let pem = der_to_pem(&der).expect("Failed to convert DER back to PEM");

        // Verify the PEM is valid
        assert!(pem.starts_with("-----BEGIN CERTIFICATE-----\n"));
        assert!(pem.ends_with("-----END CERTIFICATE-----\n"));

        // Verify round-trip consistency
        let der2 = pem_to_der(pem.as_bytes()).expect("Failed to convert PEM to DER again");
        assert_eq!(der, der2);
    }

    #[test]
    fn test_pem_to_der_invalid_pem() {
        let invalid_pem = b"not a valid pem";
        let result = pem_to_der(invalid_pem);
        assert!(result.is_err());
        assert_eq!(result.unwrap_err(), CryptoError::DerAsn1DecodeError);
    }

    #[test]
    fn test_pem_to_der_wrong_type() {
        let wrong_type_pem = b"-----BEGIN PRIVATE KEY-----\nYWJjZGVm\n-----END PRIVATE KEY-----\n";
        let result = pem_to_der(wrong_type_pem);
        assert!(result.is_err());
        assert_eq!(result.unwrap_err(), CryptoError::DerAsn1DecodeError);
    }

    #[test]
    fn test_pem_to_der_invalid_utf8() {
        let invalid_utf8 = &[0xFF, 0xFE, 0xFD];
        let result = pem_to_der(invalid_utf8);
        assert!(result.is_err());
        assert_eq!(result.unwrap_err(), CryptoError::DerAsn1DecodeError);
    }

    #[test]
    fn test_der_to_pem_empty() {
        let empty_der = &[];
        let result = der_to_pem(empty_der);
        // pem-rfc7468 should handle this gracefully
        assert!(result.is_ok());
    }
