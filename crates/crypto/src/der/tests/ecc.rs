/// Tests P-256 private key DER encoding and decoding.
///
/// Verifies that:
/// - DER-encoded private key can be decoded correctly
/// - Decoded key has correct curve type, private key, and public key coordinates
/// - Re-encoding produces identical DER output
#[test]
fn test_ecc_256_priv_key_der_encoding_decoding() {
    let der = DerEccPrivateKey::from_der(ECC_256_PRIV_KEY_DER);
    assert!(der.is_ok());
    let der = der.unwrap();
    assert_eq!(der.curve(), EccCurve::P256);
    assert_eq!(der.priv_key(), ECC_256_PRIV_KEY);
    assert_eq!(der.x(), Some(&ECC_256_X_COORD[..]));
    assert_eq!(der.y(), Some(&ECC_256_Y_COORD[..]));

    let len = der.to_der(None).unwrap();
    let mut buf = vec![0u8; len];
    let len2 = der.to_der(Some(&mut buf)).unwrap();
    assert_eq!(len, len2);
    assert_eq!(&buf[..], &ECC_256_PRIV_KEY_DER[..]);
}

