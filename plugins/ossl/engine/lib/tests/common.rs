/// Load the engine and return it
pub(crate) fn load_engine() -> Engine {
    let engine = Engine::new_engine().expect("Could not make engine");
    let id = CString::new("azihsmengine").expect("Could not make id");
    let result = bind_helper(&engine, id.as_ref());
    assert!(result.is_ok());
    engine
}

/// Wrap a key with the default unwrapping key
pub fn wrap_key(key_to_wrap: &[u8]) -> Vec<u8> {
    let mut rng = rand::thread_rng();

    // Generate a random AES-256 key
    let mut aes_key = [0u8; 32];
    rng.fill(&mut aes_key);

    // Encrypt the key to wrap using AES-256
    let kek = Kek::from(aes_key);
    let wrapped_key = kek
        .wrap_with_padding_vec(key_to_wrap)
        .expect("Could not wrap key");

    // Get the unwrapping key
    let hsm_ctx = azihsm_engine();
    let hsm_ctx_lock = hsm_ctx.read();
    let app_session = hsm_ctx_lock
        .app_session_as_ref()
        .expect("Could not get app session");
    let unwrapping_key_handle = app_session
        .get_unwrapping_key()
        .expect("Could not get unwrapping key");
    let wrapping_key = app_session
        .export_public_key(&unwrapping_key_handle)
        .expect("Could not export public key");

    // Parse the public key from DER format
    let public_key =
        RsaPublicKey::from_public_key_der(&wrapping_key).expect("Could not decode wrapping key");

    // Encrypt the AES-256 key using RSA with OAEP padding
    let padding = Oaep::new::<sha2::Sha256>();
    let encrypted_aes_key = public_key
        .encrypt(&mut rng, padding, &aes_key)
        .expect("Could not encrypt public key");

    // Combine the encoded keys
    let mut buffer: Vec<u8> = Vec::with_capacity(encrypted_aes_key.len() + wrapped_key.len());
    buffer.extend(&encrypted_aes_key);
    buffer.extend(&wrapped_key);
    buffer
}

pub fn import_key_wrapped(key: &[u8], key_usage: KeyUsage) -> EvpPKeyCtx<RsaKeyData> {
    let engine = load_engine();

    // Wrap the key to present to the HSM
    let wrapped_key = wrap_key(key);

    // Create key and ctx structures
    let pkey = EvpPKey::new().expect("Could not create pkey");
    let rsa = RsaKey::<RsaKeyData>::new().expect("Could not create RSA key");
    pkey.assign_rsa(rsa.as_mut_ptr())
        .expect("Could not assign RSA key");
    let ctx = EvpPKeyCtx::<RsaKeyData>::new(&pkey, &engine).expect("Could not create ctx");

    // Import key
    pkey_rsa_import_key(
        ctx.as_mut_ptr(),
        &wrapped_key,
        DigestKind::Sha256,
        key_usage,
        KeyAvailability::Session,
        None,
        false,
    )
    .expect("Could not import key");

    ctx
}
